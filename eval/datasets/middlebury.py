"""Middlebury stereo dataset adapter (Scharstein & Szeliski et al.).

The Middlebury pairs give rectified left/right images with dense ground-truth
disparity — the reference data for the stereo/disparity feature (thesis §5.4).

Layout expected under data/Middlebury/ (gitignored), one directory per scene:

    data/Middlebury/
      <scene>/
        im0.png        left image
        im1.png        right image
        disp0.pfm      ground-truth disparity for the left view (optional)

Download (2014 set, a few scenes are enough for spot checks):

    mkdir -p data/Middlebury && cd data/Middlebury
    for s in Adirondack Motorcycle Piano; do
      curl -O https://vision.middlebury.edu/stereo/data/scenes2014/zip/$s-perfect.zip
      unzip $s-perfect.zip
    done

The older 2003/2005/2006 sets (Tsukuba, Teddy, Cones, …) use PNG disparity maps
named disp1.png/disp2.png and are equally usable — point `iter_scenes` at them.

For CI and the golden test the framework ships a small *synthetic* pair with a
known disparity (data/test_images/stereo/, generated by
tools/make_synthetic_stereo.py); Middlebury is for qualitative depth-feature
evaluation on real imagery.
"""

from pathlib import Path

import numpy as np
from PIL import Image

DEFAULT_ROOT = Path(__file__).resolve().parents[2] / "data" / "Middlebury"


def available(root=DEFAULT_ROOT):
    return Path(root).is_dir() and any(_scene_pair(d) for d in Path(root).iterdir() if d.is_dir())


def _scene_pair(scene_dir):
    """(left, right) paths for a scene directory, or None if incomplete."""
    scene_dir = Path(scene_dir)
    for left_name, right_name in (("im0.png", "im1.png"), ("view1.png", "view5.png")):
        left = scene_dir / left_name
        right = scene_dir / right_name
        if left.exists() and right.exists():
            return left, right
    return None


def iter_scenes(root=DEFAULT_ROOT):
    """Yield (scene_name, left_path, right_path) triples."""
    root = Path(root)
    if not root.is_dir():
        raise FileNotFoundError(
            f"Middlebury not found under {root} — see this module's docstring for download steps")
    for scene_dir in sorted(p for p in root.iterdir() if p.is_dir()):
        pair = _scene_pair(scene_dir)
        if pair is not None:
            yield scene_dir.name, pair[0], pair[1]


def load_disparity(scene_dir):
    """Ground-truth disparity as float32, or None when unavailable.

    Handles PFM (disp0.pfm) and 8-bit PNG (disp1.png) conventions. PNG values
    are raw disparities (0 == unknown for several Middlebury sets)."""
    scene_dir = Path(scene_dir)
    pfm = scene_dir / "disp0.pfm"
    if pfm.exists():
        return _read_pfm(pfm)
    for name in ("disp1.png", "disp2.png"):
        png = scene_dir / name
        if png.exists():
            return np.asarray(Image.open(png).convert("L")).astype(np.float32)
    return None


def _read_pfm(path):
    """Minimal PFM reader (grayscale) for Middlebury disparity maps."""
    with open(path, "rb") as f:
        header = f.readline().rstrip()
        if header not in (b"Pf", b"PF"):
            raise ValueError(f"Not a PFM file: {path}")
        color = header == b"PF"
        dims = f.readline().split()
        width, height = int(dims[0]), int(dims[1])
        scale = float(f.readline().rstrip())
        endian = "<" if scale < 0 else ">"
        channels = 3 if color else 1
        data = np.frombuffer(f.read(), endian + "f")
        data = data.reshape(height, width, channels) if color else data.reshape(height, width)
        return np.flipud(data).astype(np.float32)
